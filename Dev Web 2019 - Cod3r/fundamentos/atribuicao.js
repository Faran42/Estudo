const a = 7 //atribuicao e não igualdade
let b = 3

b+=a // b = b + a atribuicao aditiva
console.log(b)
b-=4 // b = b - 4 atribuicao subtrativa
console.log(b)
b*=2 // b = b * 2 atribuicao multiplicativa
console.log(b)
b/=2 // b = b / 2 atribuicao divisiva
console.log(b)
b%=2 // b = b % 2 atribuicao modular (resto da divisão)
console.log(b)
